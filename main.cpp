#include <iostream>
#include <cmath>

using namespace std;

int main()
{
    // Computing the value of 2 to the power 5
    cout<< pow(3,3) << endl;
    //Finding the square-root of 36
    cout<< sqrt(36) << endl;
    //rounding number
    cout<< round(4.4) << endl;
    cout<< round(4.6) << endl;
    //ceil of number
    cout<< ceil(4.4) << endl;
    cout<< ceil(4.6) << endl;
    //floor of number
    cout<< floor(4.4) << endl;
    cout<< floor(4.6) << endl;
    //Finding bigger number
    cout<< fmax(3,10) << endl;
    //Finding minimum number
    cout<< fmin(3,10) << endl;
    return 0;
}
